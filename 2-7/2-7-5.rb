# ハッシュ(Hash)

colors = {'red' => 'ff0000','green' => '00ff00', 'blue' => '0000ff'}

# 要素を参照する
colors['blue']
colors['black']

# 別の値を格納する
colors['green'] = '008000'
colors

colors = {:red => 'ff0000',:green => '00ff00', :blue => '0000ff'}
colors[:blue]

{red: 'ff0000', green: '00ff00', blue: '0000ff'}